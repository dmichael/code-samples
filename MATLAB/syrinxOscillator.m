%% SYRINX OSCILLATOR
%%------------------------------------------------------------
function [dy] = syrinxOscillator(j,t,y,c,f0,b,k)
    dy = zeros(size(y));

    dy(1) = y(2);
    dy(2) = -k(j)*y(1)- c*y(1)^2*y(2) + b(j)*y(2) - f0;
    
